package binarydiff;

class BinaryDiffService {

    private String left;
    private String right;

    public String left() {
        return left;
    }

    public String right() {
        return right;
    }

    void setLeft(String left) {
        this.left = left;
    }

    void setRight(String right) {
        this.right = right;
    }
}
